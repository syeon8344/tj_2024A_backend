package day01; // 현재 클래스 파일이 위치한 패키지 폴더명

public class Step2 { // class start // 모든 코드는 class 안에서만 작성한다.

    // 실행코드 main + enter => main 함수 // 코드를 읽어주는 역할(스레드) 포함
    public static void main(String[] args) { // main start
        //p.45
        int level; // 정수(소수점 없는 숫자)형 변수 level 선언
        level = 10; // level 변수 호출하고 10 대입
        System.out.println(level); // 변수 호출하고 출력

        //p.46
        int level2 = 10;
        System.out.println(level2);
    }// main end
}
